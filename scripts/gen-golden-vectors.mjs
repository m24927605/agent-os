// Generate cross-language golden vectors from the TS reference implementation (provenance = TS).
// Run AFTER `pnpm run build` (imports the built dist/). Writes kernel/testdata/golden-vectors.json.
//
// The Go kernel (SLICE-P1-002) must reproduce these byte-for-byte. We deliberately do NOT let Go
// produce the vectors (that would make "Go conforms to TS" into "Go conforms to Go").
//
// NOTE: no secret-shape literal is ever written to the fixture. Redaction (by-key + by-value)
// conformance is asserted by a Go test that assembles the canary at RUNTIME (see
// kernel/internal/canonical/redaction_test.go), so the committed fixture stays secret-clean.
// The signing private key is generated in memory and never written (only public key + signature).
import { createPrivateKey, createPublicKey, sign } from "node:crypto";
import { writeFileSync } from "node:fs";
import { canonicalizeAuditEvent } from "../dist/audit/canonical.js";
import { checkpointBytes, computeEntryHash, GENESIS_PREV_HASH } from "../dist/audit/kernel/log.js";

const hex = (u8) => Buffer.from(u8).toString("hex");

// Non-secret conformance events covering §5.2 boundaries (canonicalize is structural).
const events = [
  {
    name: "basic",
    event: {
      eventId: "evt-1", requestId: "req-1", timestamp: "2026-06-19T00:00:00.000Z",
      tenantId: "tenant-a", projectId: "proj-1", taskId: "task-1", actorId: "agent:claude",
      action: "fs:read", resource: "/workspace/a", result: "denied",
      policyDecision: { effect: "deny", reason: "no matching allow rule (deny-by-default)" },
    },
    prevHash: GENESIS_PREV_HASH, sequence: 0,
  },
  {
    name: "key-order-shuffled",
    event: {
      result: "success", policyDecision: { reason: "ok", effect: "allow" }, resource: "/x",
      action: "fs:read", actorId: "a", taskId: "t", projectId: "p", tenantId: "tn",
      timestamp: "2026-06-19T00:00:00.000Z", requestId: "r", eventId: "e",
    },
    prevHash: `sha256:${"ab".repeat(32)}`, sequence: 1,
  },
  {
    name: "non-ascii-and-escapes",
    event: { resource: '中文/emoji-😀/quote-"-backslash-\\-tab-\t-newline-\n-ctrl-', note: "保留" },
    prevHash: GENESIS_PREV_HASH, sequence: 2,
  },
  {
    name: "empty-and-null",
    event: { a: {}, b: [], c: null, d: "x" },
    prevHash: GENESIS_PREV_HASH, sequence: 3,
  },
  {
    name: "large-sequence",
    event: { eventId: "e", resource: "/x" },
    prevHash: GENESIS_PREV_HASH, sequence: 9007199254740991,
  },
  {
    // Locks JS Number::toString / JSON.stringify formatting in canonical bytes (the float path):
    // integers print in full (no exponent) up to 1e21; >=1e21 and small fractions use exponential.
    name: "numbers",
    event: { nums: [0, 42, -5, 1700000000000, 0.1, 1e-7, 1e21], n: 100000000000 },
    prevHash: GENESIS_PREV_HASH, sequence: 5,
  },
];

const vectors = events.map((e) => ({
  name: e.name,
  event: e.event,
  prevHash: e.prevHash,
  sequence: e.sequence,
  canonicalBytesHex: hex(canonicalizeAuditEvent(e.event)),
  entryHash: computeEntryHash(e.event, e.prevHash, e.sequence),
}));

// Checkpoint vector: TS signs checkpointBytes(headEntryHash, length) with a DETERMINISTIC ed25519
// key derived from a fixed 32-byte seed (PKCS8 DER), so regenerating the fixture is reproducible.
// The seed is a test-only constant, not a credential; only the public key + signature are written.
const seed = Buffer.alloc(32, 7);
const privateKey = createPrivateKey({
  key: Buffer.concat([Buffer.from("302e020100300506032b657004220420", "hex"), seed]),
  format: "der",
  type: "pkcs8",
});
const publicKey = createPublicKey(privateKey);
const headEntryHash = vectors[0].entryHash;
const length = 1;
const signatureBase64 = sign(null, checkpointBytes(headEntryHash, length), privateKey).toString("base64");
const checkpoint = {
  headEntryHash,
  length,
  publicKeySpkiBase64: publicKey.export({ type: "spki", format: "der" }).toString("base64"),
  signatureBase64,
};

const out = {
  provenance: "generated by scripts/gen-golden-vectors.mjs from the TS reference (dist/)",
  genesisPrevHash: GENESIS_PREV_HASH,
  vectors,
  checkpoint,
};
writeFileSync(
  new URL("../kernel/testdata/golden-vectors.json", import.meta.url),
  `${JSON.stringify(out, null, 2)}\n`,
);
console.log(`wrote ${vectors.length} vectors + 1 checkpoint to kernel/testdata/golden-vectors.json`);
